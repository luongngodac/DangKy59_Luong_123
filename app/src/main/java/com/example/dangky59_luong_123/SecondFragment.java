package com.example.dangky59_luong_123;

import android.content.Context;
import android.content.SharedPreferences;
import android.os.Bundle;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.TextView;

import androidx.annotation.NonNull;
import androidx.fragment.app.Fragment;
import androidx.navigation.fragment.NavHostFragment;

public class SecondFragment extends Fragment {
    TextView tvStr;
    @Override
    public View onCreateView(
            LayoutInflater inflater, ViewGroup container,
            Bundle savedInstanceState
    ) {
        // Inflate the layout for this fragment
        return inflater.inflate(R.layout.fragment_second, container, false);
    }

    public void onViewCreated(@NonNull View view, Bundle savedInstanceState) {
        super.onViewCreated(view, savedInstanceState);

        tvStr = ((MainActivity)getActivity()).findViewById(R.id.tvStr);

        SharedPreferences sharedPreferences = ((MainActivity)getActivity()).getSharedPreferences(FirstFragment.sharePrefName, Context.MODE_PRIVATE);
        if(sharedPreferences != null)
        {
            String tenKhachHang = sharedPreferences.getString(FirstFragment.key_str, "");
            tvStr.setText(tenKhachHang);
        }

        view.findViewById(R.id.button_second).setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View view) {
                NavHostFragment.findNavController(SecondFragment.this)
                        .navigate(R.id.action_SecondFragment_to_FirstFragment);
            }
        });
    }
}